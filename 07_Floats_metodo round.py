#En este codigo se explicara el uso del método round() aplicado en datos tipo float

num1 = 23.345567        #Se guarda un dato tipo float con 6 decimales en la variable num1
num2 = 12.875678        #Se guarda un dato tipo float con 6 decimales en la variable num2
resultado = num1+num2   #Se guarda el resultado de la operación entre 2 datos tipo float
print("Resultado: ",round(resultado,3))       #se imprime el resultado utilizando el metodo round() al cual le especificamos que el resultado se le aplique un  redondeo a 3 decimales
